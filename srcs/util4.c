#include "../minishell.h"

int					ft_util_is_export(char chr)
{
	if ((chr >= 'a' && chr <= 'z') || (chr >= 'A' && chr <= 'Z') ||
		chr == '_' || chr == '/' || chr == '\\')
		return (1);
	return (0);
}